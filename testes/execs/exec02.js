var num01 = Number(window.prompt('Informe um valor: '));
var num02 = Number(window.prompt('Informe um segundo valor: '));

var soma = num01 + num02;
var multiplo = num01 * num02;
var subtrair = num01 - num02;
var dividendo = num01 / num02;

document.write(`Soma: ${soma} / `);
document.write(`Multiplo: ${multiplo} / `);
document.write(`Subtração: ${subtrair} / `);
document.write(`Divisão: ${dividendo} `);
